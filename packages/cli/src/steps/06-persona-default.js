// Step 6: pin a default persona on the assistant landing page.
//
// The assistant page reads its options from `listPersonaOptions()` (in
// lib/grounding.ts). We can't safely change that function from the CLI without
// risking a syntax error, so instead we drop a small `customer-defaults.json`
// next to it that the page can read at render time. The clone ships with a
// tiny patch in `lib/customer-defaults.ts` that imports it.

import path from "node:path";
import fs from "node:fs";
import fse from "fs-extra";

export async function setDefaultPersona(ctx) {
  const { outputDir, persona, dryRun } = ctx;
  if (dryRun || !persona) return { ok: false, reason: "no-persona" };

  const libDir = path.join(outputDir, "apps", "explorer-web", "lib");
  await fse.ensureDir(libDir);

  // 1. The data file the runtime reads.
  const defaultsPath = path.join(libDir, "customer-defaults.json");
  const payload = {
    customer: ctx.customer,
    customerLabel: ctx.customerLabel,
    vertical: ctx.vertical?.canonical ?? null,
    cloud: ctx.cloud?.slug ?? "duckdb",
    persona: {
      id: persona.id,
      label: persona.label,
    },
    tagline: ctx.tagline || null,
  };
  fs.writeFileSync(defaultsPath, JSON.stringify(payload, null, 2) + "\n", "utf8");

  // 2. A typed re-export so the rest of the app doesn't need to know about the
  //    JSON. We write it conditionally — if it already exists from a previous
  //    accelerator run we skip rather than overwrite (idempotent re-runs).
  const tsPath = path.join(libDir, "customer-defaults.ts");
  if (!fs.existsSync(tsPath)) {
    const ts = `// AUTO-GENERATED by @domain-explorer/init. Safe to edit.
import data from "./customer-defaults.json";

export interface CustomerDefaults {
  customer: string;
  customerLabel: string;
  vertical: string | null;
  cloud: string;
  persona: { id: string; label: string } | null;
  tagline: string | null;
}

export const customerDefaults = data as CustomerDefaults;
`;
    fs.writeFileSync(tsPath, ts, "utf8");
  }

  return { ok: true, persona: persona.id };
}
